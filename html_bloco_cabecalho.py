import html_bloco_cabecalho_IMP

def gera(title, grande):
  """Retorna o cabecalho padrão site, com o string {title} no titulo da página .
  O parâmetro booleano {grande} especifica o tamanho do título e do cabeçalho:
  {True} é apropriado para a página de entrada, {False} para as demais páginas."""
  return html_bloco_cabecalho_IMP.gera(title, grande)

